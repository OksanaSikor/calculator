package page;

public class Calculator {

    public Calculator() {
    }

    public int addition(int one, int two) {
        return one + two;
    }

    public int subtraction(int one, int two) {
        return one - two;
    }

    public int multiplication(int one, int two) {
        return one * two;
    }

    public int division(int one, int two) {
        return one / two;
    }
}

